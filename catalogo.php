        <center><h1>Airbus 319, Airbus 320 y Airbus 321</h1></center>
        <p>El Airbus 319 es una aeronave mixta que transporta mercancía en su bodega. La diferencia frente al Airbus 320 y al Airbus 321 es que en el Airbus 319 carga que se transporta va suelta.
            Por su parte, el Airbus 320 y el Airbus 321 tienen un rango operativo que va desde los 3500 a los 5600 km. Este tipo de avión puede cargar pallets con un tamaño de la base mayor que una mayor aeronave, pero la altura de dichos pallets debe ser más reducida.
            El Airbus 320 puede cargar un total de 4 pallets más carga suelta en sus bodegas, teniendo una capacidad de carga de 1,5 toneladas y un volumen de 19 m3.</p>
        
